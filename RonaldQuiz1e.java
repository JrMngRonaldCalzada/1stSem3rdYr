package FirstYearSecondSem;

public class RonaldQuiz1e 
{
	public static void main(String[] args) 
	{
		double[] ronald = {3.5, 5.5, 4.52, 5.6};
		for(int kalabasa = 0; kalabasa < ronald.length; kalabasa++)
		{
			ronald[0] = 3.5;
			ronald[1] = 5.5;
			ronald[2] = 4.52;
			ronald[3] = 5.6;
			System.out.print(ronald[kalabasa] + " ");
		}
		System.out.println();
	}
}